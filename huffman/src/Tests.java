    public void encodeReturnsStringOfBits() throws Exception
    {
        Encoder encoder = new Encoder();
        String encoded = encoder.encode("./data.txt");
        assertTrue(encoded instanceof String);
        for (int i = 0; i < encoded.length(); i ++) {
            assertTrue(Character.isDigit(encoded.charAt(i)));
        }
    }

